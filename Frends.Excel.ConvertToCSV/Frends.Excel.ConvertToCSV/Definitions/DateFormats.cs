namespace Frends.Excel.ConvertToCSV.Definitions;

/// <summary>
/// DateFormats.
/// </summary>
public enum DateFormats
{
    /// <summary>
    /// default value
    /// </summary>  
    DEFAULT,

    /// <summary>
    /// day/month/year
    /// </summary>
    DDMMYYYY,

    /// <summary>
    /// month/day/year
    /// </summary>
    MMDDYYYY,

    /// <summary>
    /// year/month/day
    /// </summary>
    YYYYMMDD
}