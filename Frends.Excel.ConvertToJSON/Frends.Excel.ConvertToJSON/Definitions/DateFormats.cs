namespace Frends.Excel.ConvertToJSON.Definitions;

/// <summary>
/// Date formats enum.
/// </summary>
public enum DateFormats
{
    /// <summary>
    /// default value
    /// </summary>
    DEFAULT,
    /// <summary>
    /// day/month/year
    /// </summary>
    DDMMYYYY,
    /// <summary>
    /// month/day/year
    /// </summary>
    MMDDYYYY,
    /// <summary>
    /// year/month/day
    /// </summary>
    YYYYMMDD
}
